public class StringExercise {
	public static void main(String[] args) {
		String phrase = "We don't need no education\nWe don't need no thought control";
		System.out.println(phrase);

		String phrase2 = "Check \"this\" out!, \"s inside of \"s!";
		System.out.println(phrase2);

		String phrase3 = "In windows, the main drive is usually C:\\";
		System.out.println(phrase3);

		String phrase4 = "I can do backslashes \\, double backslashes \\\\,\n" +
				"and the amazing triple backslash \\\\\\!";
		System.out.println(phrase4);
	}
}
